function poi(
  id: string,
  name: string,
  address: string,
  lat: number,
  lng: number,
  distanceM: number,
  kind: MapPoiKind,
  keyword: string,
  rating?: number,
  avgPrice?: number,
  lateNightFriendly = false,
): RawMapPoi {
  return {
    id,
    name,
    address,
    lat,
    lng,
    category: kind === 'hotel' ? '酒店宾馆' : kind,
    distanceM,
    kind,
    keyword,
    rating,
    avgPrice,
    lateNightFriendly,
  };
}

function venueParkingAnchor(
  activityLegacyId: number,
  name: string,
  address: string,
  lat: number,
  lng: number,
): VenueParkingAnchor {
  return {
    activityLegacyId,
    id: `venue-parking-${activityLegacyId}`,
    name,
    address,
    lat,
    lng,
    category: 'venue parking access',
    distanceM: 0,
    distanceLabel: '场地交通定位锚点；活动日请以主办方停车指引为准',
    kind: 'parking',
    keyword: 'parking',
    lateNightFriendly: false,
  };
}

export function getHotPathFallbackPois(
  activityLegacyId: number,
  kind?: MapPoiKind,
  keyword?: string,
): RawMapPoi[] {
  const all = FALLBACK_BY_ACTIVITY.get(activityLegacyId);
  if (!all?.length) return [];

  return all.filter((p) => {
    if (kind) {
      if (p.kind === kind) {
        // exact kind match
      } else if (kind.startsWith('nightlife')) {
        if (p.kind !== 'nightlife_food' || p.keyword !== '夜宵') return false;
      } else {
        return false;
      }
    }
    if (keyword) {
      const k = keyword.trim();
      if (k && !p.keyword.includes(k) && !k.includes(p.keyword)) {
        return false;
      }
    }
    return true;
  });
}

export function getAllHotPathFallbackPois(
  activityLegacyId: number,
): RawMapPoi[] {
  return [...(FALLBACK_BY_ACTIVITY.get(activityLegacyId) ?? [])];
}
